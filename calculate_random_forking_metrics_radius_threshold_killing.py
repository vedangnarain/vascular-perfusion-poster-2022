# Define function to compute the average of all trials in a radius threshold selection
def compute_average_threshold(solver_name, alpha_value, threshold, max_trials, hypoxic_threshold_list, plot, read):
    
    # Create table to store all the threshold data trials in an alpha group
    threshold_table = np.array([])

    # Extract metrics from all trials and store in table
    for trial_selection in range(1, max_trials+1):    
#        print(layout_selection)
        hypoxic_fraction_list, mean_value, min_value, half_value, max_value, std_value = get_distribution_stats(solver_name, alpha_value, str(trial_selection), str(threshold), hypoxic_threshold_list, plot, read)
        table_entry = np.hstack([hypoxic_fraction_list, mean_value, min_value, half_value, max_value, std_value])
        threshold_table = np.vstack([threshold_table, table_entry]) if threshold_table.size else table_entry

    # Return the hypoxic fractions, mean, min, 50%, max, and std for a threshold averaged across all layouts
    return np.average(threshold_table, axis=0)
#    return np.average(threshold_table[0]), np.average(threshold_table[1]), np.average(threshold_table[2]), np.average(threshold_table[3]), np.average(threshold_table[4])

# Define a function to return statistics for all the heterogeneities in the data
def get_solver_stats(solver_name, alpha_list, radius_threshold_list, max_trials, hypoxic_threshold_list, plot, read):
#    table = np.array([])
    alpha_table = np.array([])
    for alpha_value in alpha_list:
        for radius_threshold in radius_threshold_list:    
            average_threshold_data = compute_average_threshold(solver_name, alpha_value, radius_threshold, max_trials, hypoxic_threshold_list, plot, read)
            table_entry = np.hstack([float(alpha_value), float(radius_threshold), average_threshold_data])
            alpha_table = np.vstack([alpha_table, table_entry]) if alpha_table.size else table_entry
    return alpha_table


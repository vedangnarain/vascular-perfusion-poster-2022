# Define a function to generate the data for a single alpha value
def get_alpha_line(alpha_group, max_radius):
    
    # Compute the averages for the betas in the alpha group
    pq_table = np.array([])
    alpha_threshold_grouped = alpha_group.groupby(alpha_group.threshold)
    for threshold in range(0, max_radius+1):
        pq_table_entry = np.array([])
        alpha_threshold_group = alpha_threshold_grouped.get_group(threshold)
        pq_table_entry = np.array([alpha_threshold_group["alpha"].mean(), alpha_threshold_group["threshold"].mean(), alpha_threshold_group["PQ"].mean()])
        pq_table = np.vstack([pq_table, pq_table_entry]) if pq_table.size else pq_table_entry
    
#     Plot the alpha data
#    ax.plot(pq_table[:,0], pq_table[:,1], pq_table[:,2])
    
#     Return the table for reference
    return pq_table

